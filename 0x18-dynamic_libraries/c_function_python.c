#include "main.h"
/**
 * add - adds two intergers
 * @a: first input
 * @b: second input
 * Return: the sum
*/
int add(int a, int b)
{
return (a + b);
}
/**
 * sub - substract two intergers
 * @a: first input
 * @b: second input
 * Return: the difference
*/
int sub(int a, int b)
{
return (a - b);
}
/**
 * mul - multiplies two intergers
 * @a: first input
 * @b: second input
 * Return: the result
*/
int mul(int a, int b)
{
return (a * b);
}
/**
 * div - divide two intergers
 * @a: first input
 * @b: second input
 * Return: the result
*/
int div(int a, int b)
{
return (a / b);
}
/**
 * mod - calculates the remainder of div of two intergers
 * @a: first input
 * @b: second input
 * Return: the result
*/
int mod(int a, int b)
{
return (a % b);
}
